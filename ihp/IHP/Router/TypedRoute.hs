-- | Build the OpenAPI document for a path or query parameter declared by the
-- route DSL.
mkRouteParameterDoc ::
    forall field value.
    ( KnownSymbol field
    , Typeable.Typeable value
    , ToSchema value
    ) =>
    ParameterLocation ->
    Bool ->
    ParameterDoc
mkRouteParameterDoc location required =
    ParameterDoc
        { parameterName = cs (symbolVal (Proxy @field))
        , parameterLocation = location
        , parameterRequired = required
        , parameterSchema = Proxy @value
        , parameterTypeRep = Typeable.typeRep (Proxy @value)
        }
{-# INLINE mkRouteParameterDoc #-}

-- | Method lookup generated by the @[routes|...|] DSL for typed GADT actions.
class HasActionMethods action where
    actionMethods :: action -> Maybe [StdMethod]

-- | Returns the HTTP methods accepted by the generated route for an action
-- value. This is used by typed form helpers to choose the HTML form method and
-- method override field.
typedActionMethods :: forall action. (HasActionMethods action) => action -> Maybe [StdMethod]
typedActionMethods = actionMethods
{-# INLINE typedActionMethods #-}

runTypedRouteAction ::
    forall application controller body response.
    ( Controller controller
    , ControllerAction controller ~ TypedControllerAction body response
    , RunTypedControllerAction body (ControllerAction controller) response
    , InitControllerContext application
    , ?application :: application
    , Typeable.Typeable application
    , Typeable.Typeable controller
    ) =>
    Maybe DocumentedRenderExpectation ->
    Status ->
    controller ->
    Application
runTypedRouteAction renderExpectation successStatus controller waiRequest waiRespond =
    earlyReturnMiddleware
        ( \request respond -> do
            context <- setupActionContext @application (Typeable.typeOf controller) request respond
            let ?theAction = controller
            let ?context = context
            let ?respond = respond
            let ?request = context.request
            let ?modelContext = ?request.modelContext
            authenticatedModelContext <- prepareRLSIfNeeded ?modelContext
            let ?modelContext = authenticatedModelContext
            beforeAction
            runTypedControllerAction @body @(ControllerAction controller) @response successStatus (action controller)
        )
        (attachOpenApiRenderExpectation renderExpectation waiRequest)
        waiRespond
{-# INLINE runTypedRouteAction #-}

typedDocumentedRenderExpectationForResponse ::
    forall response.
    ( Typeable.Typeable response
    , ViewSupport.JsonView response
    , JSON.ToJSON (ViewSupport.JsonResponse response)
    , ToSchema (ViewSupport.JsonResponse response)
    ) =>
    DocumentedRenderExpectation
typedDocumentedRenderExpectationForResponse =
    let response = Proxy @response
     in DocumentedRenderExpectation
        { expectedViewTypeName = cs (show (Typeable.typeRep response))
        , expectedTypedJson = fmap (JSON.toJSON . ViewSupport.json) . fromDynamic @response
        , expectedJsonSchemaValidationError = join . fmap (SchemaValidation.validatePrettyToJSON . ViewSupport.json) . fromDynamic @response
        }
{-# INLINE typedDocumentedRenderExpectationForResponse #-}
