-- | Build the OpenAPI document for a path or query parameter declared by the
-- route DSL.
mkRouteParameterDoc ::
    forall field value.
    ( KnownSymbol field
    , Typeable.Typeable value
    , ToSchema value
    ) =>
    ParameterLocation ->
    Bool ->
    ParameterDoc
mkRouteParameterDoc location required =
    ParameterDoc
        { parameterName = cs (symbolVal (Proxy @field))
        , parameterLocation = location
        , parameterRequired = required
        , parameterSchema = Proxy @value
        , parameterTypeRep = Typeable.typeRep (Proxy @value)
        }
{-# INLINE mkRouteParameterDoc #-}

class DummyRouteValue value where
    dummyRouteValue :: value

instance DummyRouteValue Text where
    dummyRouteValue = ""
    {-# INLINE dummyRouteValue #-}

instance DummyRouteValue Int where
    dummyRouteValue = 0
    {-# INLINE dummyRouteValue #-}

instance DummyRouteValue Integer where
    dummyRouteValue = 0
    {-# INLINE dummyRouteValue #-}

instance DummyRouteValue Bool where
    dummyRouteValue = False
    {-# INLINE dummyRouteValue #-}

instance DummyRouteValue UUID where
    dummyRouteValue = nil
    {-# INLINE dummyRouteValue #-}

instance DummyRouteValue value => DummyRouteValue (Maybe value) where
    dummyRouteValue = Nothing
    {-# INLINE dummyRouteValue #-}

instance DummyRouteValue [value] where
    dummyRouteValue = []
    {-# INLINE dummyRouteValue #-}

instance (DummyRouteValue (PrimaryKey table)) => DummyRouteValue (Id' table) where
    dummyRouteValue = Id dummyRouteValue
    {-# INLINE dummyRouteValue #-}

-- | Method lookup generated by the @[routes|...|] DSL for typed GADT actions.
class HasActionMethods action where
    actionMethods :: action -> Maybe [StdMethod]

-- | Returns the HTTP methods accepted by the generated route for an action
-- value. This is used by typed form helpers to choose the HTML form method and
-- method override field.
typedActionMethods :: forall action. (HasActionMethods action) => action -> Maybe [StdMethod]
typedActionMethods = actionMethods
{-# INLINE typedActionMethods #-}

runTypedRouteAction ::
    forall application controller.
    ( Controller controller
    , InitControllerContext application
    , ?application :: application
    , Typeable.Typeable application
    , Typeable.Typeable controller
    ) =>
    Maybe DocumentedRenderExpectation ->
    controller ->
    Application
runTypedRouteAction renderExpectation controller waiRequest waiRespond =
    earlyReturnMiddleware
        ( \request respond -> do
            context <- setupActionContext @application (Typeable.typeOf controller) request respond
            let ?context = context
            let ?respond = respond
            let ?request = context.request
            let ?modelContext = ?request.modelContext
            runAction controller
        )
        (attachOpenApiRenderExpectation renderExpectation waiRequest)
        waiRespond
{-# INLINE runTypedRouteAction #-}

typedDocumentedRenderExpectationForAction ::
    forall controller body response.
    ( Controller controller
    , ControllerAction controller ~ ActionDef controller body response
    ) =>
    controller ->
    Maybe DocumentedRenderExpectation
typedDocumentedRenderExpectationForAction typedAction =
    let ?context = error "typedDocumentedRenderExpectationForAction: action documentation must not use ?context"
     in let ?modelContext = error "typedDocumentedRenderExpectationForAction: action documentation must not use ?modelContext"
         in let ?theAction = typedAction
             in let ?respond = error "typedDocumentedRenderExpectationForAction: action documentation must not use ?respond"
                 in let ?request = error "typedDocumentedRenderExpectationForAction: action documentation must not use ?request"
                    in typedDocumentedRenderExpectation <$> actionDefDoc (action typedAction)
{-# INLINE typedDocumentedRenderExpectationForAction #-}

typedDocumentedRenderExpectation ::
    forall controller body response.
    TypedActionDoc controller body response ->
    DocumentedRenderExpectation
typedDocumentedRenderExpectation (TypedActionDoc _ _ _ _ _ (response :: Proxy response) _ _) =
    DocumentedRenderExpectation
        { expectedViewTypeName = cs (show (Typeable.typeRep response))
        , expectedTypedJson = fmap (JSON.toJSON . ViewSupport.json) . fromDynamic @response
        , expectedJsonSchemaValidationError = join . fmap (SchemaValidation.validatePrettyToJSON . ViewSupport.json) . fromDynamic @response
        }
{-# INLINE typedDocumentedRenderExpectation #-}

typedRouteDocumentForAction ::
    forall controller body response.
    ( Controller controller
    , ControllerAction controller ~ ActionDef controller body response
    ) =>
    Text ->
    Text ->
    [StdMethod] ->
    [ParameterDoc] ->
    controller ->
    Maybe TypedRouteDocument
typedRouteDocumentForAction typedRouteName typedRoutePathPattern typedRouteMethods typedRouteParameters docAction =
    let ?context = error "typedRouteDocumentForAction: action documentation must not use ?context"
     in let ?modelContext = error "typedRouteDocumentForAction: action documentation must not use ?modelContext"
         in let ?theAction = docAction
             in let ?respond = error "typedRouteDocumentForAction: action documentation must not use ?respond"
                 in let ?request = error "typedRouteDocumentForAction: action documentation must not use ?request"
                     in case actionDefDoc (action docAction) of
                            Nothing -> Nothing
                            Just typedActionDoc ->
                                Just
                                    TypedRouteDocument
                                        { typedRouteDocumentName = typedRouteName
                                        , typedRouteDocumentPath = typedRoutePathPattern
                                        , typedRouteDocumentMethods = typedRouteMethods
                                        , typedRouteDocumentParameters = typedRouteParameters
                                        , typedRouteDocumentActionDoc = typedActionDoc
                                        }
{-# INLINE typedRouteDocumentForAction #-}
